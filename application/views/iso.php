        <h1 align="Center">ISO 20121</h1>
        <p>Todas as ações que são desenvolvidas em prol do meio ambiente precisam ser aplaudidas, principalmente quando essas ações têm o objetivo de criar uma corrente de pessoas que também tem esse objetivo. A ISO 20121 é exatamente um desses casos.</p>
        <p>Seu nome completo <b><i>é ISO 20121:2012, Sistemas de gestão para a sustentabilidade de eventos – Requisitos com orientações de uso.</i></b> O código dessa nova certificação no Brasil será ABNT NBR ISO 20121:2012</p>
        <p>A ISO 20121 é relacionada a eventos sustentáveis. Na verdade trata-se de uma norma de gerenciamento. A ISO foi publicada em 2012.</p>
        <p>&nbsp;</p>
        <p><b>Aplicação da ISO 20121</b></p>
        <p>Ela se aplica a eventos de diferentes esferas. Desde pequenas festas internas até mesmo eventos grandes. Podemos destacar:</p>
        <ul>
            <li>Eventos esportivos – campeonatos, torneios, festivais, Copa do Mundo, Jogos Olímpicos;</li>
            <li>Eventos políticos – campanhas políticas, festas partidárias;</li>
            <li>Eventos sociais – festas, formaturas;</li>
            <li>Eventos culturais – exposições, shows, apresentações, concertos;</li>
            <li>Eventos empresariais – conferências, treinamentos, processos seletivos;</li>
            <li>Eventos filantrópicos – bazares beneficentes, leilões solidários;</li>
        </ul>
        <p>A ideia é desenvolver políticas em prol do planeta e fazer dos eventos acontecimentos sustentáveis. Ou seja, que eles consigam não somente ser um sucesso naquilo que se propõe a fazer, como também consigam reduzir ou eliminar o seu impacto ao planeta e a comunidade em que ele está ocorrendo.</p>
        <p><b>Métricas ISO 20121</b></p>
        <p>Essa norma oferece uma série de mecanismos que indicam onde está sendo feito algo em desacordo com o meio ambiente ou com a comunidade. E também indica como trabalhar para que seja feita a redução ou ainda a neutralização desses impactos. Ele não fala só em contenção e neutralização de impactos, mas também de como maximizar os fatos positivos como os efeitos sem prol da comunidade como a geração de vagas de trabalho. O modelo de gestão é baseado em duas das normas mais aplicadas. ISO 9001 e 14001.</p>
        <p>Mas como um evento pode ser prejudicial?</p>
        <p>Embora possam gerar emprego, proporcionando renda a uma parcela de pessoas, gerando economia, ainda existem impactos que precisam ser reduzidos. Listamos alguns dos impactos, que de modo geral, são causados na realização de eventos.</p>
        <p><b>Eventos geram:</b></p><b>
        </b><p><b></b></p>
        <ul>
            <li>Consumo alto de água;</li>
            <li>Produção de resíduos sólidos que podem ser reutilizados;</li>
            <li>Consumo de energia;</li>
            <li>Efeitos sobre a biodiversidade, principalmente festivais musicais realizados próximos às áreas verdes;</li>
            <li>Desperdício de materiais, ou seja, como existe uma margem de lucro, alguns materiais que poderiam ser aproveitados acabam sendo descartados, muitas vezes de forma indevida. Além disso, o lixo consumido em um evento nem sempre é separado para a coleta seletiva;</li>
            <li>Impactos na comunidade local – por exemplo barulho ou ainda geração de muito resíduo. Podemos destacar também, a mudança do perfil de frequentadores da região e, isso, de certa forma pode impactar o cotidiano dessas pessoas;</li>
            <li>Impactos causados pelo transporte em massa.</li>    
        </ul>
        <p><b>Toda a cadeia está inserida:</b></p>
        <p>A norma não é aplicada única e exclusivamente para o evento em si, mas para toda a sua cadeia. Ou seja, inclui também os fornecedores. Portanto para obter a certificação, todas a entidades envolvidas, sejam elas públicas ou privadas, precisam desenvolver ações de redução de impacto.</p>
        <p><b>Eventos certificados</b></p>
        <ul>
            <li>Em 2013 o Rock in Rio foi o primeiro evento na América Latina a receber o Certificado na América Latina. Uma case é sempre uma das formas de compreender melhor como a aplicação de uma ação pode favorecer seu negócio. Acesse o <a href="http://rockinriolisboa.sapo.pt/wp-content/uploads/sites/5/2014/04/Rock-in-Rio-Lisboa-2014_Principios-Valores-e-Politica-de-Sustentabilidade.pdf?5590fb">link </a>saiba com os organizadores desse evento conseguiram se certificar.</li>
        </ul>
        <ul>
            <li>Os jogos Olímpicos de Londres seguiram as normas da ISO. Na verdade, eles foram o primeiro evento a seguir as determinações da ISO, servindo como parâmetros para as competições esportivas seguintes.</li>
        </ul>
        <p>&nbsp;</p>
        <p><b>Vantagens da ISO 20121</b></p>
        <p>Podemos indicar como principais vantagens da ISO 20121:</p>
        <ul>
            <li>A sustentabilidade é incorporada a identidade do evento;</li>
            <li>Existe redução de custos operacionais, já que é feito o controle sobre o desperdício dos materiais e uma melhor utilização;</li>
            <li>Melhora a imagem da organizadora ou do próprio evento perante a sociedade como um todo;</li>
            <li>Redução de emissão de gases do efeito estufa;</li>
            <li>Legado para a comunidade, ou seja, o evento deixa algo de positivo que pode ser aproveitado pela comunidade onde ele foi realizado;</li>
            <li>A metodologia permite que nenhum ponto seja deixado de lado, ou seja, é sustentabilidade verdade;</li>
            <li>Exploração de marketing em prol da construção da marca do evento.</li>  
        </ul>

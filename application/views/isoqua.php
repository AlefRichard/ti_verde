        <p align="center" style="alignment-baseline: central; font-family: cursive; font-size: large;;">
        <h1 align="center">ISO 14000</h1>
  
        <p>A ISO 14000 tem como principal foco a gestão ambiental e fornece uma estrutura organizada para que as empresas consigam promover ações internas para obter a certificação.</p>
        <p>A ISO 14000 é auditada pelo ISO (International Organization for Standardization), que é a autoridade em certificações em todo mundo.</p>
        <p>No Brasil, a primeira empresa certificada foi a Bahia Sul Celulose S.A. em 1996. Aqui,a certificação é mantida pela ABNT e, portanto, foi denominada ABNT NBR ISO 14001</p>
        <h4>O que é avaliado pela ISO 14000</h4>
        <ul>
            <li>Auditorias ambientais</li>
            <li>Avaliação de desempenho ambiental</li>
            <li>Rotulagem ambiental</li>
            <li>Análise o ciclo de vida dos produtos</li>
        </ul>
        <p>Vale ressaltar que a empresa precisa seguir a legislação ambiental do pais em que está inserida, afinal a certificação é realizada com base naquilo que o pais considera como correto.</p>
        <h4>Quais organizações podem ser beneficiadas com o ISO 14000</h4>
        <p>Qualquer organização pode ser beneficiada, mas é preciso solicitar junto aos órgãos competentes os documentos necessários. Empresas privadas e órgãos públicos podem solicitar a ISO, não existe restrição nesse sentido.</p>
        <p>Existem dois cenário onde a empresa pode iniciar o processo de certificação ISO 14000:</p>
        <ol>
            <li>Declara estar apta a certificação, por já ter realizado as ações necessárias para isso.</li>
            <li>Quando irá iniciar o processo de instalação dos projetos com o objetivo de obter a certificação.</li>
        </ol>
        <h4>Existe prazo de validade para o ISO 14000?</h4>
        <p>O ISO 14000, assim como outros modelo de ISO, tem duração de três anos. Após esse período de tempo é realizada nova auditoria para se comprovar a continuidade do projeto ou solicitar adequações que precisam ser realizadas. </p>
        <h4>A implementação do ISO 14000 propicia melhorias nas empresas</h4>
        <p>Quando a empresa utiliza a certificação ela agrega valor à marca e a seus produtos, pois estará associada com seu nome uma imagem de organização que se preocupa com a natureza.<br>
        Além disso, em muitos casos existe ganho econômico, pois as empresas passam a reutilizar recursos (como água) e a utilizar energia elétrica, por exemplo, de forma menos agressiva ao planeta e isso se reflete na redução das contas mensais.</p>
        <h4>A ISO 14000 é subdividida em outras ISOs, cada uma com suas características próprias:</h4>
        <p><b>ISO 14001:</b> trata do Sistema de Gestão Ambiental (SGA), sendo direcionada à certificação por terceiras partes.</p>
        <p><b>ISO 14004:</b> trata do Sistema de Gestão Ambiental, sendo destinada ao uso interno da empresa, ou seja, corresponde ao suporte da gestão ambiental.</p>
        <p><b>ISO 14010:</b> são normas sobre as Auditorias Ambientais. São elas que asseguram credibilidade a todo processo de certificação ambiental, visando as auditorias de terceiras partes, nas quais se verificam os compromissos estabelecidos pela empresa em seu Sistema de Gestão Ambiental.</p>
        <p><b>ISO 14031:</b> são normas sobre Desempenho Ambiental, que estabelecem as diretrizes para medição, análise e definição do desempenho ambiental de uma organização, a fim de assegurar o SGA.</p>
        <p><b>ISO 14020:</b> são normas sobre Rotulagem Ambiental, estabelecendo orientações para a expressão das características ambientais dos produtos das empresas, de forma que os rótulos ressaltem as características ambientais do produto.</p>
        <p><b>ISO 14040:</b> são normas sobre a Análise do Ciclo de Vida, estabelecendo as interações entre as atividades produtivas e o meio ambiente. Analisa o impacto causado pelos produtos, processos e serviços relacionados desde a extração dos recursos naturais até a disposição final.</p>
        </p>
